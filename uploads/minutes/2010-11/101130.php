<p align="center"><font size="3" face="Times New Roman"><b>PRESENTATION: 
University of Michigan Space Physics Research Lab REU</b></font></p>
<p align="center"><font size="3" face="Times New Roman"><b>By Robert 
Nelson</b></font> <br></p>
<p><font size="3" face="Times New Roman">Rob partook in this internship 
over the summer of 2010 in Ann Arbor, Michigan. There, he met grad students 
from all across the world and worked on a project dealing with mercury 
deposition. Of interest was the different, if any, types of convective 
patterns that existed with high deposition events. The results were 
then presented to faculty and other students at the university.  </font></p>
<p><font size="3" face="Times New Roman">The deadline to apply is March 
1<sup>st</sup>. While juniors and seniors are preferred, all are welcome 
to apply. For additional details, see the attached PowerPoint or contact 
Rob.</font> <br> <br></p>

