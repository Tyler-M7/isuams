<p align="center"><font size="3" face="Times New Roman"><b>SENIOR THESIS 
AWARD</b></font> <br></p>
<p><font size="3" face="Times New Roman">Carl Jacobson and other faculty 
members presented the award for best senior thesis report. As a background, 
senior thesis (or Mteor 499) is very important and unique in that it 
allows you to solve your own problems on an extended basis &#8211; much 
like what would be expected with jobs in the field. For those yet to 
take thesis, view it as a challenge.</font></p>

<p><font size="3" face="Times New Roman">All seniors should be very 
proud of what they&#8217;ve accomplished, as many of the results were fascinating. </font> <br>
</p>
<p><font size="3" face="Times New Roman"><u>Best senior thesis</u>: 
Ryan Spies! Title: August 2010 Flood Inundation Mapping Using GIS and 
LIDAR Data along the Squaw Creek in Ames, IA. He raised questions on 
how flooding impacts may be mitigated for the Ames community and ISU. 
He used certain tools to examine the extent of flooding in the Squaw 
creek and the surrounding areas from the model&#8217;s interpretation when 
land surface features were changed. </font></p>
<p><font size="3" face="Times New Roman"><u>Runner Up</u>: Ben Moser. 
Title: Comparative Analysis of the 1993, 2008, and 2010 Warm Seasons: 
Analysis of Low-Level Jets and Synoptic Features. </font></p>
<p><font size="3" face="Times New Roman"><u>Second Runner Up</u>: Casey 
Zoellick. Title: Comparing Extreme Precipitation Events Between the 
MM5I Climate Model and Observations using the Method for Object-Based 
Diagnostic Evaluation (MODE). </font> <br> <br></p>
