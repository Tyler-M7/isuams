<p align="center"><font size="3" face="Times New Roman"><b>PRESENTATION: Paths Taken by Meteorology Students</b></font></p>
<p align="center"><font size="3" face="Times New Roman"><b>By Elise and Chris Shultz</b></font> <br></p>
<p><font size="3" face="Times New Roman">Main points discussed involved the path they took and many options for current students as well as 
many tips. The presentation is attached for further details, but here are some points to be taken away:</font></p>

<ul type="DISC">
  <li><font size="3" face="Times New Roman">Try to figure out a general direction you want to go, but leave all options open. For example, 
  even if you&#8217;re not interested in broadcast, give Cy&#8217;s Eyes a try.</font></li>
  <li><font size="3" face="Times New Roman">It&#8217;s never too early to start looking forward. </font></li>
  <li><font size="3" face="Times New Roman">Understand and identify your weaknesses within the field, as they may come back to haunt you if 
  you don&#8217;t address them early on. Example is radar information &#8211; not really taught in-depth at ISU.</font></li>
  <li><font size="3" face="Times New Roman">Network, network, network! Join the National NWA, get involved with LinkedIn, have business 
  cards available, etc. </font></li>
  <li><font size="3" face="Times New Roman">Expand your horizons by volunteering for projects outside your comfort zone. </font></li>
</ul>

