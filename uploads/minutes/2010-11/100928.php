<p><font size="3" face="Times New Roman">*Past President Rachel Hatteberg informed the club that the club recently received a phone call 
from the president of the national AMS who revealed that <b>our club has been awarded</b> <b>chapter of the year for 2009-2010</b>! To 
receive such an award, a club must excel at (among other things) demonstrating professional development, participating in community outreach 
events, and contributing to the national AMS. Last time our club received the award was back in 2005-2006. This is an outstanding 
achievement!!!</font> <br>
 <br></p>

<p><font size="3" face="Times New Roman"><b>*Closets Collide Information: </b>
Jennifer Sonner: Closets collide is a new sustainable fashion group that started this past year and serves both the ISU campus and the Ames 
community. Our club has been invited to collaborate with them on Oct. 23 in the Sun Room of the MU for a &#8220;clothing swap&#8221;. We&#8217;d have three 
options: </font></p>
<p><font size="3" face="Times New Roman">1.) Set up a booth to promote our club at this clothing swap (couldn&#8217;t sell anything, but could put 
up tornado machine, etc). </font></p>
<p><font size="3" face="Times New Roman">2.) Set up a workshop and join forces with the Society of Women Engineers (SWE).</font></p>
<p><font size="3" face="Times New Roman">3.) Set up our own workshop. We&#8217;d work with Jennifer or another art coordinator to incorporate 
fashion sustainability with weather somehow. </font></p>
<p><font size="3" face="Times New Roman">We are about environment and they feel we&#8217;d be a good addition to what they do. They ask that a 
decision either way should be made before October 8<sup>th</sup>. Those interested should contact JoBeth at <a href="mailto:pres.isuams@iastate.edu" target="_blank">
pres.isuams@iastate.edu</a>. </font> <br>

