<p align="center"><font size="3" face="Times New Roman"><b>Presentation for Hollings Scholarship</b></font> <br></p>
<ul type="DISC">
  <li><font size="3" face="Times New Roman">JoBeth Minniear and Robert Nelson are both Hollings scholars. Benefits: $16,000 scholarship over 
  junior and senior year and a summer internship of $6,500 dollars. You&#8217;d have a NOAA mentor, get research experience, free conference 
  travel, and can meet peers in similar fields. </font></li>

  <li><font size="3" face="Times New Roman">Requirements include status as a US citizen, 3.0 + GPA, and must be a sophomore to apply.</font></li>
  <li><font size="3" face="Times New Roman">Applications accepted Nov. 2<sup>nd</sup> until Jan 29<sup>th</sup>. </font></li>
  <li><font size="3" face="Times New Roman">Need to complete two essays (generally about your future plans, and need 1000 characters which 
  is really only a couple paragraphs) and collect two references among other criteria. </font></li>
  <li><font size="3" face="Times New Roman">Very competitive: Approximately 600-700 apply and around 100 get selected. Don&#8217;t have a stellar 
  GPA? Don&#8217;t worry it&#8217;s not everything. Essays, extracurricular activities and recommendations are also a large part of the selection 
  process.</font></li>

