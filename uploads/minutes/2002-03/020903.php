<p>Intramurals: Brian Tentinger will work on getting a list of upcoming
intramurals. The ones coming up right away are Curling and Volleyball. Brian
will be sending out an email to let everyone know when to sign up with him to
participate. AMS doesn't have a great history in intramurals, and would like to
see some more teams participate and win!</p>
<p>Historian: Jon Hobbs will be continuing to work on the scrapbook. If any past
members have pictures that they could donate to the scrapbook, let Jon know. He
will also be working on the bulletin board outside 3128.</p>
<p>Fundraising: We would like to form a fundraising committee this year, and do
some more fundraising stuff... We need money to have food at the meetings and to
take trips! If you are interested in being on this committee, there will be a
signup for it at the next meeting. Some fund raising ideas are a car wash, and
Jolesch Photography. Other ideas welcome!</p>
<p>Trips: We would also like to have a committee to plan more trips this year.
Some trips we could take would be a TV station in Des Moines, the National
Weather Service in Des Moines, Offutt AFB in Omaha, and museums with weather
exhibits in the area. The committee will also help to plan the spring break
trip. We would like to go to Florida this year, but if that doesn't work out, we
would probably go to the Norman Oklahoma area where there are many weather
related places to visit. Sign up for this committee will be at the next meeting
also.</p>
<p>Webpage: Kevin Sullivan is the webmaster for this year. He will be updating
the website in the next week. The website address is </font>
<a HREF="http://www.meteor.iastate.edu/ams/index.html">
<u>http://www.meteor.iastate.edu/ams/index.html</u></font></a></p>

<p>Speakers: We will be working on getting some speakers in for meetings this
year. Some possibilities are Dr. Gallus, Jason Parkin from KCCI-TV in Des
Moines, and someone from the National Weather Service.</p>
<p>School Talks: We have for the last few years been doing school talks to 4th
grade students in the Ames schools during severe weather week in April. We plan
to continue doing this this year. We will also be sending an announcement to all
the lower elementary schools to volunteer to do school talks throughout the
year. Possibly one or two per month...depending on requests.</p>
<p>Storm Spotter Training: We would like to do a storm spotter training course
once again in the spring. We did this for the first time last year, and had a
wonderful turnout. The training is open to anyone. Andy Kula from the NWS came
last year to do the training along with Dr. Gallus.</p>
