<p><font size="3" face="Garamond"><b>Presentation</b></font> <br>

</p>
<p><font size="3" face="Garamond">The group went to Ft. Worth on first 
day. While there, they went to six flags and stayed at the Best Western 
Beach Front Hotel. They then toured an extremely nice TV station and 
visited a Weather Museum in Galveston. The museum had a lot of fun weather 
information and gadgets. Wednesday, the Spring Breakers visited the 
NASA facility and got to see the old mission control center, the new 
mission control center, the Spaceflight meteorology group, and the Mach-up 
facility (where astronauts train). Sadly, they did not get to actually 
meet any astronauts. Since it was really nice on Wednesday, they went 
to the beach and enjoyed the sun. But while there, they could not resist 
doing some derivations and station plots in the sand. They also got 
to see some Hurricane Ike damage while in Galveston. On Thursday, the 
group made its way north to Norman, OK. While there, they visited NSSL, 
SPC, and the Norman WFO offices. While at the WFO, they realized there 
was a big snow storm headed their way. They tried to beat this home, 
but sadly got stuck in Kansas on Friday due to snow. They did eventually 
make it home safely. </font> <br></p>
