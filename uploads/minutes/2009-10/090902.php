<p><font size="3" face="Times New Roman"><b>
Cy.s Eyes on the Skies Co-Producers</b></font></p>

<p><font size="3" face="Times New Roman">
Any Mair and Ryan Alliss informed the club of ISU.s very own weather show. It is a 20-minutes segment on 
Monday and Wednesday nights at 6:30. If you are interested in broadcast or just want to help out backstage 
and meet people, please attend the first show. We will be meeting in the Agron parking lot at 5:45pm to walk 
to the building. Prep work begins at 6 and we.re on-air at 6:30. Cy.s Eyes is a .Weather Channel. type of 
show. There are 4 segments, including a host, weather news, Iowa weather and National Weather. It is one of 
the most professional weather shows in a college setting. If you have any doubts of what you want out of 
meteorology, please try Cy.s Eyes. It is a great experience and will help you get a job when you graduate. 
</font></p>

