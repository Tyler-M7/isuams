<p><font size="3" face="Garamond">If you are not a member of the Central 
Iowa National Weather Association, please consider looking into it at 
<a href="http://www.iowa-nwa.com/" target="_blank">http://www.iowa-nwa.com/</a>. Our chapter works very closely with the NWA, 
and it is a great way to meet professionals in meteorology: broadcast, 
National Weather Service, ISU faculty members, and other areas. You 
also can help plan the Severe Storm Conference held every spring. You 
can join committees to help decide who gives the presentations, help 
figure out travel, work on the electronics at the conference, work registration, 
and other things. The NWA also takes field trips (hopefully to a wind 
farm this year) and has presentations at meetings. By being a member 
of the NWA, you get chances to receive numerous scholarships, a large 
discount for the Severe Storm Conference, and stay informed of the latest 
weather news. The Central Iowa NWA has meetings once a month in Ames 
or Ankeny, and we will provide rides for these meetings. If you have 
any questions or are interested in joining, please contact JoBeth (</font><a href="mailto:secretary.isuams@iastate.edu" 
target="_blank"><font color="#0000FF" size="3" face="Garamond"><u>secretary.isuams@iastate.edu</u></font></a><font size="3" 
face="Garamond">) or Rachel Hatteberg (<a href="mailto:president.isuams@iastate.edu" 
target="_blank">president.isuams@iastate.edu</a>)<WBR>.</font> <br>

