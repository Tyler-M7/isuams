<p><font size="3" face="Garamond">Rachel began the meeting with an update 
on Storm the Dorms. We have been in contact with David Bunker (our contact 
in the ISU Department of Residence for this project) and are waiting 
for him get the signs. As a reminder, Storm the Dorms consists of us 
posting signs around the dorms for severe weather events. If you are 
interested in helping out, please email Rachel at </font><a href="mailto:pres.isuams@iastate.edu" 
