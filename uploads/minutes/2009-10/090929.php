<p><font size="3" face="Garamond">Next, the snowfall contest has begun! 
This is an easy contest that everyone can participate in. This contest 
involves you trying to predict when the first inch of snow will fall 
in Ames this year. All you need to do is sign up in Agronomy 3008. Put 
your name under the date on the calendar and please, only one person 
per day. Also, the last day to sign up is October 16<sup>th</sup>. The 
winner will receive a $25 gift card to a store in Ames!!</font> <br>
</p>
<p><font size="3" face="Garamond"> Since the ISU AMS homepage has Model 
Animator, Matt gave a short tutorial on how to use it. At the ISU AMS 
homepage, click on .Member Resources. then .Model Animator.. 
The username is .********. and the password is .*****.. There 
are four model runs and many different models, including the NAM and 
GFS. You can look at any surface in the atmosphere and can look at up 
to 16days in the GFS. You do not have to clear your current loop to 
add another. This way you can compare the similarities and differences 
in the models.</font> <br></p>
