<p><font size="3" face="Garamond">Ryan gave a WxChallenge update. He 
showed how to submit a forecast: go to the ISU AMS Homepage, click on 
Forecast Contest, National Contest, click on .enter contest. in 
the top left corner of the page. Enter your username and password (email 
Ryan if you don.t remember), and enter your forecast: max and min 
temps, wind (in knots), and precipitation (in inches). On the left side 
of the page, there are a lot of links to help you forecast. Take some 
time to play around with these links and they will be very useful to 
you. Ask an upperclassman if you would like help understanding the data. 
Don.t forget to pay your $5 to Justin if you are participating!</font> <br>
