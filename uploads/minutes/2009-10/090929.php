<p><font size="3" face="Garamond">Next, she discussed the continuation 
of the HyVee grocery bag project. This is a project we began last year. 
We printed a template with safety precautions for severe weather events. 
Last year, it was limited to mostly the Ames area, but we would like 
to expand it to Ankeny and the Des Moines area. We asked the Central 
Iowa Chapter of the National Weather Association to help us get the 
word out and they have agreed to assist us. These bags will be put out 
around Severe Weather Awareness Week and will stay out until they are 
gone. We will be using last year.s design but altering the dates.</font> <br>
</p>
<p><font size="3" face="Garamond">Lastly, Rachel announced the 90<sup>th</sup> 
AMS Annual Meeting in Atlanta, GA this January. This meeting is for 
