<p><font size="3" face="Garamond"><b>NCAR Presentation by Shannon Rabideau</b></font> <br>
</p>
<p><font size="3" face="Garamond">We concluded the meeting with a presentation 
by Shannon about the NCAR Undergraduate Leadership Workshop in Boulder, 
CO. She attended this workshop this June for a week and met people from 
a variety of majors from all over the United States. She was nominated 
by the ISU faculty for this workshop and accepted.</font> <br>
</p>
<p><font size="3" face="Garamond">During this great opportunity, Shannon 
heard from many speakers from different areas of study, including: climate, 
satellite technology, forecasting, etc. She also participated in many 
activities during the week. These activities included: tours of many 
of the NCAR facilities in Boulder, Rocky Mountain National Park, several 
NCAR labs, and other interesting places in Boulder.</font> <br>
</p>
<p><font size="3" face="Garamond">The main ideas Shannon took away from 
the workshop were:</font></p>
<ul type="DISC">
  <li><font size="3" face="Garamond">May end up in completely 
  different career path than what you planned on</font></li>

  <li><font size="3" face="Garamond">Take opportunities as they 
  come... they generally lead to new, interesting, and beneficial outcomes</font></li>
  <li><font size="3" face="Garamond">The future of meteorology 
  is diversification, the combo of many fields of study</font></li>
  <ul type="DISC">
    <li><font size="3" face="Garamond">Think outside the box</font></li>
    <li><font size="3" face="Garamond">Don.t be afraid to pursue 
    what really interests you, even if you have to create a new program 
    of study</font></li>
    </ul>
  <li><font size="3" face="Garamond">Get to know others in your 
  field.they may have valuable insight</font></li>

</ul>
