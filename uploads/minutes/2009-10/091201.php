<p><font size="3" face="Garamond">The 14<sup>th</sup> Annual Severe 
Storms and Doppler Radar Conference in Des Moines at the Sheraton Hotel 
will be held March 25-27 this year. The non-member student rate is $75 
and the student member rate is $55. Last year we received partial reimbursements 
for the conference and are hoping to receive them again from the LAS 
Council this year. This conference is a lot of fun and a great experience. 
Broadcast, National Weather Service employees, researchers, and other 
meteorologists will be present. It is a great way to meet professionals 
in the meteorology field! It has beginner workshops, as well as advanced 
talks. You can stay in the Sheraton, but this is not required. At night, 
we usually go out with other students and professionals in the field.</font> <br>

