<p><font size="3" face="Garamond"><b>Presentation - Justin Wittrock </b></font> <br>
</p>
<p><font size="3" face="Garamond">Last summer, Justin participated in 
the National Weather Center Research Experience for Undergraduates Program 
in Norman, OK.</font> <br></p>
<p><font size="3" face="Garamond">What is NWC REU? It is a chance to 
experience research in the meteorology field. All majors are accepted 
into this program. It can be viewed as stepping stones for your senior 
thesis project . a great way to get an early start! It also looks 
great on resume.</font> <br></p>
<p><font size="3" face="Garamond">Justin.s project was on the Viability 
of Weather Dissemination via Social Network Technologies. More information 
on it can be found at </font><a href="http://www.caps.ou.edu/reu/reu09/projects" 
target="_blank"><font color="#0000FF" size="3" 
face="Garamond"><u>www.caps.ou.edu/reu/reu09/<WBR>projects</u></font></a><font size="3" 
face="Garamond">.</font> <br></p>

<p><font size="3" face="Garamond">Different Trips taken during the program 
include:</font></p>
<p><font size="3" face="Garamond">Ponca City . see airplanes with 
weather instruments mounted</font></p>
<p><font size="3" face="Garamond">VORTEX2 . meet people involved in 
it. See vehicles with radar</font></p>
<p><font size="3" face="Garamond">Oklahoma City . outdoor activities</font></p>
<p><font size="3" face="Garamond">Route 66 . Pops . Over 500 
kinds of pop to choose from</font></p>
<p><font size="3" face="Garamond">Wichita.s Mountains Reserve . 
see prairie dogs</font> <br></p>
<p><font size="3" face="Garamond">How do you apply? The application 
can be found at </font><a href="http://www.caps.ou.edu/reu/application.html" 
target="_blank"><font color="#0000FF" size="3" 
face="Garamond"><u>www.caps.ou.edu/reu/<WBR>application.html</u></font></a><font size="3" 
face="Garamond">. It also has ideas for different projects 
listed. When filling out the application, you choose top 3 project choices. 
You may apply after one year of college.</font> <br></p>

