          <p>NCWFC<br>
            Fairbanks, Alaska is the current city. This is a difficult city to
            forecast for. The reporting station is just north of the river that
            runs through Fairbanks &#8211; and the city itself lies in a bowl
            north of the tallest mountains in North America. This tends to create
            some tricky forecasting &#8211; stagnant weather, very warm downslope
            winds, and so forth.<br>
            Sites to visit for the Fairbanks round include &lt;www.arh.noaa.gov&gt;
            and &lt;pafc.arh.noaa.gov/model.php&gt;<br>
            You can also talk to Morgan Brown (mebrown [at] iastate [dot] edu)
            if you have any Alaska forecasting questions.</p>
