<p><font size="3" ><b>Storm Ready</b>-we will be assigning 
groups and team leaders to try and start making some progress in making 
the Iowa State University more storm ready.  This will consist 
of posting signs about where to go during a severe weather event.  
Most signs that are posted now are out of date and many buildings don?t 
even have directions on where to go during severe weather events.  
This is another way that we as an organization can get involved in our 
community and do our best to try and keep the public safe during these 
events.  </font></p>
