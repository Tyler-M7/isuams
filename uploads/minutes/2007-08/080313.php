<ul><p align="justify"><font size="3" face="Times New Roman">Trinity 
Consultants works with environmental qualities using air dispersion 
modeling software.  They work with many private clients and there 
is lots of on the job training.  Travel is required for some clients.  
New hires perform modeling, regulatory studies, emission calculations 
and permit application writing.  As a meteorologist you are expected 
to perform as an expert with atmospheric motions as they are vital in 
air pollution forecasting.  Some benefits are few weekend or rotating 
overnight shifts.  For more information or questions please contact 
Angie Wanger at: </font><a href="mailto:awanger@trinityconsultants.com" target="_blank"><font color="#0000FF" size="3" face="Times New Roman"><u>awanger@trinityconsultants.com</u></font></a><font size="3" face="Times New Roman">.  Reimbursement of Angie Wanger 
