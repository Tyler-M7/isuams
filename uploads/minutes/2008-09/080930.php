  She will be on campus, visiting WISE (Women in Science and Engineering), 
  and we would like to set up a time to meet and talk with her.  
  The proposed time to meet her is 4 PM.  There was a vote put up 
  for a time, and 4 PM passed.  A biography will be sent out soon.</font></li>
