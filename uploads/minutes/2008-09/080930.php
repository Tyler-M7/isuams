  and identify areas to put .storm shelter. signs.  We will need 
  some manpower to do this.  These signs will be put under some plexi-glass 
  so they won.t be stolen.  We will also talk about educating the 
  CA.s about what to do if there is a tornado warning.  If you 
  didn.t sign up for this, shoot President Schwedler an email to get 
  involved.</font></li>
</ul>
