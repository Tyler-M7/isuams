<p><font size="3" face="Times New Roman"><b>President Schwedler</b></font></p>
<ul type="DISC">
  <li><font size="3" face="Times New Roman">President Schwedler 
  began discussing the Central Iowa National Weather Association Severe 
  Storms and Doppler Radar Conference, with speakers on Hurricane Ike, 
  the Parkersburg, IA EF5, and the Midwest flooding.  CINWA is also 
  featuring Tim Samaras.  The conference will be held from 2 April 
  to 4 April in West Des Moines at the Sheraton Hotel.  A sign-up 
  sheet was sent out for people who are interested.  We.ll also 
  be having a sign-up for people attending the ISU Alumni Luncheon.  
  Meet some of our faculty and alumni.  The conference fee is 70 
  dollars, which includes a meal, but not the stay at the Sheraton.  
  The cost of staying at the Sheraton is 102 dollars a night split four 
  ways.  </font></li>

  <li><font size="3" face="Times New Roman">We.ll tour WHO 
  Channel 13 news weather station.  We are planning to go in middle 
  February.  The proposed dates were February 10, 11, or 12.  
  A vote was held to see who would be interested, and when they will be 
  available.  We.ll determine if 12 February would work best.</font></li>
  <li><font size="3" face="Times New Roman">CPR training has 
  also been planned, with a tentative date for 21 February.  The 
  training should last roughly 3-4 hours.  There will be a fee required 
  for the renting of the materials.</font></li>
  <li><font size="3" face="Times New Roman">We will also have 
  Spring clothing sales soon.  Please send your creative, funny, 
  intriguing, and/or educational ideas to <a href="http://pres.isuams.iastate.edu" target="_blank">pres.isuams.iastate.edu</a>.</font></li>
</ul>
