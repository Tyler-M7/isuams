<p><font size="3" face="Times New Roman"><b>Geological and Atmospheric 
Sciences</b></font></p>
<ul type="DISC">
  <li><font size="3" face="Times New Roman">We first held the 
  Undergraduate research symposium award presentation.  Dr. Chen 
  and Dave Flory were present to give out awards.  The winners of 
