money for our chapter.</font> <br> <br> <br></p></ul>
<ul><p><font size="3" face="Times New Roman">Here are the proposed changes:</font> <br>
</p></ul>
<ul><p><font size="3" face="Times New Roman">ARTICLE IV, 3d.  <br>
 <br>

The Sophomore Chair is charged with collaborating with the Social Chair
and acting as a liaison between underclassmen and upperclassmen.  In
the absence of the Social Chair, the Sophomore Chair will report social
events to the chapter. <br>
 <br>
ARTICLE IV, 3e. (if 3d passes; otherwise, it is 3d) <br>
 <br>
The Academic Chair is charged with compiling a list of courses chapter
members are enrolled in and organizing study/review sessions for the
chief undergraduate courses.  The Academic Chair will also appoint chapter
members to lead study/review sessions if they volunteer to do so and
will collaborate with these session leaders.  The Academic Chair is to
inform chapter members of upcoming atmospheric science seminars.</font> <br>
</p></ul>
<ul><p><font size="3" face="Times New Roman">Addition to ARTICLE IV,
b.</font></p></ul>
<ul><p><font size="3" face="Times New Roman"> <br>
The vice-president shall attend the Liberal Arts and Sciences council
meetings. If the vice-president is unable to fulfill said duty, the
President shall appoint a member of the chapter to attend the council
meetings.</font> <br></p></ul>

