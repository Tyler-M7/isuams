<p><font size="3" face="Times New Roman">Constitutional Amendments:</font></p>
<ul><p><font size="3" face="Times New Roman">-We talked about adding
the Sophomore Chair and Academic Chair positions to the constitution.</font></p></ul>
<ul><p><font size="3" face="Times New Roman">-We also want to add a
