<p>ISU AMS Meeting Minutes<br>
http://www.meteor.iastate.edu/ams/<br>
September 13, 2001</p>

<P>Ahhh...what an AMS meeting. We laughed, we cried, we stuffed our faces full
of pizza. Man, y'all is sloppy. Anyway, here's what happened at the meeting last
night.</P>
<P>First of all, if you haven't paid dues to the chapter, you can participate in
the meetings, but you cannot take part in fun activities held by the group or
vote in elections. Dues are $5.00. Pay Penny ASAP.</P>
<P>The Iowa State-Iowa game has been postponed due to what happened this past
Tuesday. Because of the postponement, the planned tailgate will be held October
20th, which is homecoming weekend.</P>
<P>The National Forecasting Contest will begin a week from Monday (September
24). Deadline for payment and sign-up was tonight. The first official city to
forecast for is Richmond, VA. Mike Falk will get an online bulletin board set up
before the beginning of the first official forecasting period. Our daily scores
will also be sent to Texas A&amp;M for unofficial scoring.</P>
<P>A first 1&quot; snow contest will occur this year for the first time.
Participants can choose up to three days to forecast when the first inch of snow
will fall at the National Weather Service in Johnston (suburb of Des Moines).
Entries are $1.00 a piece and entries should be sent to Jeremy or Penny by
Thursday, September 27. 35% of the winnings will go to the winner, 15% will go
to the runner-up, and the other 50% will go back into the AMS. (In case of a
tie, the winning money will be split.)</P>
<P>Our schedule trip to Offutt Air Force Base in Omaha has been indefinitely
postponed (It may be rescheduled for sometime in the spring). As an alternative,
we will look into going to a television station and the National Weather Service
office in Johnston. The tentative date to do this will be sometime in October or
November. Justin will also be in charge of getting a speaker for some meeting
this semester.</P>
<P>A bowling activity was decided to be held very soon. Jeremy and Kevin will
search out possible dates. A possibility is to have this activity as early as
later this month. Jeremy will email everyone with the chosen date and for final
sign-up.</P>
<P>We also want to have a spring break trip this year. Possible locations are
Florida or Atlanta, GA. The Central Iowa NWA Severe Storms conference is at the
tail end of spring break week, so if the trip takes too long, then those people
that go, might not be able to go to the conference (something to keep in mind).</P>
<P>A new chroma key wall will be built for ISU4 and Cy's Eyes on next Saturday
(September 22). If you want to help, contact Ryan (rwest@iastate.edu) ASAP.</P>
<P>I (your fabulous secretary) will find out if we can have a bulletin board
placed somewhere in the hallway. The board will house meeting times, meeting
minutes, upcoming events, and such.</P>
<P>The pop fund is back up and running. There are sodas in the closet of 3128
for 50 cents a piece. A container will be set up in the near future to keep the
money. Money paid to the group comes back to us so we can do fun stuff, so
PLEASE remember to pay for every pop you take.</P>
<P>In intramural news, the men's flag football team didn't fare very well (it's
not good when it's four on seven, now is it). They lost 52-8. Basically, we need
guys to play on the team. Next game is Monday, September 17 at 6:00 pm. The game
will be played at the fields east of the football stadium. Show up 10-15 minutes
before gametime.</P>
<P>And the most important note, the amendments to the ISU AMS constitution have
been accepted by the members of the AMS-Iowa State chapter. The constitution
will be posted, in its entirety, on the website shortly. That is all from your
outstanding secretary. Now go learn something!!!</P>
<P>Jonathan Lowe</P>
<P>ISU AMS</P>
<P>secretary</P>

